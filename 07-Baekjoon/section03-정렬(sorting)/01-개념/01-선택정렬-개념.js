function selectionSort(arr) {
  for (let i = 0; i < arr.length; i++) {
    let minIndex = i // κ°€μ¥ μ‘μ€ μ›μ†μ μΈλ±μ¤
    for (let j = i + 1; j < arr.length; j++) {
      if (arr[minIndex] > arr[j]) {
        minIndex = j
      }
    }
    // μ¤μ™€ν”„(swap)
    let temp = arr[i]
    arr[i] = arr[minIndex]
    arr[minIndex] = temp
  }
}
