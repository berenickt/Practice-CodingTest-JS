/*** 트리(Tree) 용어
 * 루트 노드(root node) : 부모가 없는 최상위 노드
 * 단말 노드(leaf node) : 자식이 없는 노드
 * 깊이(depth)          : 루트 노드에서의 길이(length)
 *
 * 트리(tree)에서는 부모와 자식 관계가 성립한다
 */

