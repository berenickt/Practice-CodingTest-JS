/**
 * 숫자열인 입력데이터 2개를 받아 나누기 기능을 하는 함수를 만들어야 합니다.
 * - 표현식, 선언식, 화살표 함수로 모두 만들줄 알아야 합니다.
 * - 함수의 이름과 입력데이터는 자유롭게 지어도 됩니다.
 */
function divide(num1, num2) {
  return num1 / num2;
}

const divide = function (num1, num2) {
  return num1 / num2;
};

const divide = (num1, num2) => {
  return num1 / num2;
};

divide(10, 2); // 5
